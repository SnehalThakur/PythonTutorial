# Calculator performs all basic calculations

def sum(num1, num2):
    return num1 + num2


def difference(num1, num2):
    return num1 - num2


def multiply(num1, num2):
    return num1 * num2


def divide(num1, num2):
    return num1 / num2


def mod(num1, num2):
    return num1 % num2
    



    
